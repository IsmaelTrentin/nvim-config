-- Diagnostic
vim.diagnostic.config {
  virtual_text = true,
  signs = true,
  underline = true,
  update_in_insert = true, -- This avoids too-aggressive diagnostics while typing
  severity_sort = true,
}
vim.api.nvim_create_autocmd({ 'InsertLeave', 'TextChanged' }, {
  callback = function()
    vim.diagnostic.setloclist { open = false }
  end,
})
